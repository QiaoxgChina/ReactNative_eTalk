/**
 * 根据id查找Friend
 * @param id
 */
export function getFriendById(id) {

    for (let i = 0; i < friendList.length; i++) {
        let peopleList = friendList[i];
        for (let y = 0; y < peopleList.data.length; y++) {
            let people = peopleList.data[y];
            if (people.id.toString() === id) {
                return people;
            }
        }
    }
}

export function getFriendCount() {
    let count = 0;
    friendList.map(item => {
        item.data.map(friend => {
            if (friend.show) {
                count = count + 1;
            }
        });
    });
    return count;
}
